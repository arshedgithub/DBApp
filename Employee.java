public class Employee {
    private int id;
    private String name;
    private String gender;

    public Employee(){}

    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return this.gender;
    }

    public void setGender(int gender) {
        this.gender = gender;
    }
}